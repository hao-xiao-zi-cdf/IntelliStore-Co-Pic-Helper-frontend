    showTotal: (total) => `ÂÖ± ${total} Êù°`,
  }
})

// Ëé∑ÂèñÊï∞ÊçÆ
const fetchData = async () => {
  const res = await getPictureListUsingPost({
    ...searchParams,
    nullSpaceId: true,
  })
  message.success('Ëé∑ÂèñÊï∞ÊçÆÊàêÂäü')
  if (res.data.code === 200 && res.data.data) {
    dataList.value = res.data.data.records ?? []
    total.value = res.data.data.total ?? 0
  } else {
    message.error('Ëé∑ÂèñÊï∞ÊçÆÂ§±Ë¥•Ôºå' + res.data.message)
  }
}

// È°µÈù¢Âä†ËΩΩÊó∂ËØ∑Ê±Ç‰∏ÄÊ¨°
onMounted(() => {
