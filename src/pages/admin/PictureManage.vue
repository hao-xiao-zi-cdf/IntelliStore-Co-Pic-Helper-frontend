})

// Ëé∑ÂèñÊï∞ÊçÆ
const doSearch = () => {
  // ÈáçÁΩÆÊêúÁ¥¢Êù°‰ª∂
  searchParams.current = 1
  fetchData()
}

// Ë°®Ê†ºÂèòÂåñÂ§ÑÁêÜ
const doTableChange = (page: any) => {
  searchParams.current = page.current
  searchParams.pageSize = page.pageSize
  fetchData()
}

// Âà†Èô§Êï∞ÊçÆ
const doDelete = async (id: number) => {
  if (!id) {
    return
  }
  const res = await pictureDeleteUsingDelete({ id: id })
  if (res.data.code === 200) {
    message.success('Âà†Èô§ÊàêÂäü')
    // Âà∑Êñ∞Êï∞ÊçÆ
    fetchData()
  } else {
    message.error('Âà†Èô§Â§±Ë¥•')
  }
}

const handleReview = async (record: API.Picture, reviewStatus: number) => {
  const reviewMessage =
    reviewStatus === PIC_REVIEW_STATUS_ENUM.PASS ? 'ÁÆ°ÁêÜÂëòÊìç‰ΩúÈÄöËøá' : 'ÁÆ°ÁêÜÂëòÊìç‰ΩúÊãíÁªù'
  const res = await pictureReviewUsingPost({
    id: record.id,
    reviewStatus,
    reviewMessage,
  })
  if (res.data.code === 200) {
    message.success('ÂÆ°Ê†∏Êìç‰ΩúÊàêÂäü')
    // ÈáçÊñ∞Ëé∑ÂèñÂàóË°®
    fetchData()
  } else {
    message.error('ÂÆ°Ê†∏Êìç‰ΩúÂ§±Ë¥•Ôºå' + res.data.message)
  }
}

// Â§çÂà∂Âà∞Ââ™Ë¥¥Êùø
const copyToClipboard = async (text: string) => {
  try {
    await navigator.clipboard.writeText(text)
    message.success('Â∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥Êùø')
  } catch (err) {
    message.error('Â§çÂà∂Â§±Ë¥•')
  }
}

// Ëé∑ÂèñÂÆ°Ê†∏Áä∂ÊÄÅÈ¢úËâ≤
const getReviewStatusColor = (status: number) => {
  switch (status) {
    case PIC_REVIEW_STATUS_ENUM.PASS:
      return 'green'
    case PIC_REVIEW_STATUS_ENUM.REJECT:
      return 'red'
    default:
      return 'orange'
  }
}
